package chapter01.ex01;

public class Helloworld {

	public static void main(String[] args) {
		// 주석 : 한 라인 주석 입니다. 
			//주석은 프로드램에서 실행되지 않는 부분
		
		/*
		 * 여러 라인 주석은 /* 입니다.
		 */
		
		System.out.println("여기가 출력 부분입니다.");
		System.out.println("출력 2 입니다.");
		System.out.println("1\t2");
		//syso + <control + spacebar>
	}

}
